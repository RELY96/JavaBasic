package j01_basic;

public class Ex01_MyInfo {

	public static void main(String[] args) {
		//이름, 전직 또는 전공분야, 수강이유, 희망취업분야(포부)
		
		String name;
		String job;
		String reason;
		String plan;
		
		name="엄희정";
		job="일반 사무직";
		reason="내가 원하는대로 프로그램 만드는게 흥미로워서";
		plan="미정, 배우면서 정하고 싶습니다";
		
		System.out.println("이름 => "+name);
		System.out.println("전직 => "+job);
		System.out.println("이유 => "+reason);
		System.out.println("포부 => "+plan);
		
	} // main

} //class
